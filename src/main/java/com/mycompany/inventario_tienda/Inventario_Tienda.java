package com.mycompany.inventario_tienda;

import java.sql.Connection;
import java.sql.SQLException;
import models.Conexion;
import models.Producto;
import models.Tienda;

/**
 *
 * @author JOHAN VARON
 */
public class Inventario_Tienda {
    public static void main(String[] args) throws SQLException {

        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * CONEXION BASE DE DATOS
         *////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        Connection conn = null;
        Conexion conx = new Conexion();
        conn = conx.getConexion();
        conx.ejecutarStatement("", conn);
        
        
        
        
        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * INGRESO DE LOS 4 PRODUCTOS 
         *////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        Producto p1;
        p1 = new Producto("PAPELERIA", 1033, "LAPIZ", 1000, 20, 10);
        Producto p2;
        p2 = new Producto("SUPERMERCADO", 1133, "LECHE", 5600, 20, 10);
        Producto p3;
        p3 = new Producto("SUPERMERCADO", 1134,  "ACEITE", 10400, 30, 15);
        Producto p4;
        p4 = new Producto("DROGUERIA", 1233,  "ACETAMINOFEN", 2000, 100, 50);
        
        
        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * INFORMACION DE CADA PRODUCTO METODO TO STRING
         *////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        System.out.println("\nPRODUCTOS\n-------------------------------------------------------------------");
        System.out.println(p1.toString() + p2.toString() + p3.toString() + p4.toString());

        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * INGRESO DE LOS 4 PRODUCTOS A UNA TIENDA
         *////////////////////////////////////////////////////////////////////////////////////////////////////////////

        Tienda tienda;
        tienda = new Tienda(p1, p2, p3, p4);

        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * VENTAS
         */////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        tienda.venderProducto("LAPIZ", 15);
        tienda.venderProducto("LECHE", 23);
        tienda.venderProducto("ACEITE", 16);
        tienda.venderProducto("ACETAMINOFEN", 10);

        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * PRECIO FINAL POR PRODUCTO
         */////////////////////////////////////////////////////////////////////////////////////////////////////////////

        tienda.precioTotalProductos();

        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * GANANCIA TOTAL
         */////////////////////////////////////////////////////////////////////////////////////////////////////////////
        
        System.out.println("\nGANANCIAS TOTALES\n---------------------------------------------\n" + tienda.getGananciasTotales());
        
        /*/////////////////////////////////////////////////////////////////////////////////////////////////////////////
         * PROMEDIO DE VENTAS
         */////////////////////////////////////////////////////////////////////////////////////////////////////////////

        System.out.println(tienda.promedioVentas());
        
        /*///////////////////////////////////////////////////////////////////////////////////////////
         * PRODUCTO MAS VENDIDO
         *///////////////////////////////////////////////////////////////////////////////////////////

        System.out.println(tienda.getProductoMasVendido().toString());

        /*///////////////////////////////////////////////////////////////////////////////////////////
         * PRODUCTO MENOS VENDIDO
         *///////////////////////////////////////////////////////////////////////////////////////////

        System.out.println(tienda.getProductoMenosVendido().toString());

        /*///////////////////////////////////////////////////////////////////////////////////////////
         * ESTADO DE LOS PRODUCTOS QUE NECESITAN PEDIDO
         *///////////////////////////////////////////////////////////////////////////////////////////

        tienda.estadoPedidos();

        /*///////////////////////////////////////////////////////////////////////////////////////////
         * REALIZAR PEDIDO
         *///////////////////////////////////////////////////////////////////////////////////////////

        tienda.realizarPedido("LAPIZ", 20);
        tienda.realizarPedido("LECHE", 20);
        tienda.realizarPedido("ACEITE", 20);

        /*///////////////////////////////////////////////////////////////////////////////////////////
         * ESTADO DE PRODUCTOS
         *///////////////////////////////////////////////////////////////////////////////////////////

        tienda.estadoPedidos();

        /*///////////////////////////////////////////////////////////////////////////////////////////
         * PRODUCTOS CON DETALLE
         *///////////////////////////////////////////////////////////////////////////////////////////

        System.out.println("\nPRODUCTOS\n-------------------------------------------------------------------");
        System.out.println(p1.toString() + p2.toString() + p3.toString() + p4.toString());
    }
}


