    private void RegistrarVenta()
    {
        for (int i = 0; i < jTableVentas.getRowCount(); i++) {
            int codigo = Integer.parseInt(jTableVentas.getValueAt(i, 0).toString());
            String descripcion = jTableVentas.getValueAt(i, 1).toString();
            int cantidad = Integer.parseInt(jTableVentas.getValueAt(i, 2).toString());
            double v_unitario = Double.parseDouble(jTableVentas.getValueAt(i, 3).toString());
            double IVA = Double.parseDouble(jTableVentas.getValueAt(i, 4).toString());
            double v_total = Double.parseDouble(jTableVentas.getValueAt(i, 5).toString());
            v.setCodigo(codigo);
            v.setDescripcion(descripcion);
            v.setCantidad(cantidad);
            v.setV_Unitario(v_unitario);
            v.setIVA(IVA);
            v.setV_Total(v_total);
            vent.RegistrarVenta(v);
        }
    }
    
    private void ActualizarStock()
    {
        for (int i = 0; i < jTableVentas.getRowCount(); i++) {
            int codigo = Integer.parseInt(jTableVentas.getValueAt(i, 0).toString());
            int cantidad = Integer.parseInt(jTableVentas.getValueAt(i, 2).toString());
            p = product.BuscarProductos(codigo);
            int stockActual = p.getCantidadBodega() - cantidad;
            product.ActualizarStock(stockActual, codigo);
        }
    }
    
    private void EsNecesarioHacerPedido()
    {
        for (int i = 0; i < jTablePedidos.getRowCount(); i++) {
            int Stock = Integer.parseInt(jTablePedidos.getValueAt(i, 3).toString());
            int StockMinimo = Integer.parseInt(jTablePedidos.getValueAt(i, 4).toString());
            if (Stock < StockMinimo) {
                JOptionPane.showMessageDialog(null, "Debe realizar pedido de " + jTablePedidos.getValueAt(i, 2).toString());
            }
        }

    }
    
    private void ActualizarStockPedido()
    {
            int codigo = Integer.parseInt(jTextFieldCodigo_Pedidos.getText());
            int cantidad = Integer.parseInt(jTextFieldCantidad_Pedido.getText());
            p = product.BuscarProductos(codigo);
            int stockActual = p.getCantidadBodega() + cantidad;
            product.ActualizarStock(stockActual, codigo);
    }
    
    private void GananciaTotal()
    {
        ganancia_Total = 0.00;
        for (int i = 0; i < jTableVentas_Estadisticas.getRowCount(); i++) {
            int cantidad = Integer.parseInt(jTableVentas_Estadisticas.getValueAt(i, 2).toString());
            double v_unitario = Double.parseDouble(jTableVentas_Estadisticas.getValueAt(i, 3).toString());
            double ganancia = v_unitario * cantidad;
            ganancia_Total = ganancia_Total + ganancia;
        }
    }
