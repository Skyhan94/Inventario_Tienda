    public void ListarProducto()
    {
        List<Producto> ListarP = product.ListarProductos();
        modelo = (DefaultTableModel) jTableProducto.getModel();
        Object[] ob = new Object[6];
        for (int i = 0; i < ListarP.size(); i++) 
        {
            ob[0] = ListarP.get(i).getTipo();
            ob[1] = ListarP.get(i).getCodigo();
            ob[2] = ListarP.get(i).getNombre();
            ob[3] = ListarP.get(i).getValorUnitario();
            ob[4] = ListarP.get(i).getCantidadBodega();
            ob[5] = ListarP.get(i).getCantidadMinima();
            modelo.addRow(ob);
        }
        jTableProducto.setModel(modelo);
    }

