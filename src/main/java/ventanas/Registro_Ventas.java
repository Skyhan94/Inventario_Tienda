    private void jTextFieldVentas_CodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldVentas_CodigoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(jTextFieldVentas_Codigo.getText())) {
                int Cod = Integer.parseInt(jTextFieldVentas_Codigo.getText());
                p = product.BuscarProductos(Cod);
                if (p.getNombre() != null) {
                    jTextFieldVentas_Tipo.setText(""+p.getTipo());
                    jTextFieldVentas_Nombre.setText(""+p.getNombre());
                    jTextFieldVentas_Precio.setText(""+p.getValorUnitario());
                    jTextFieldVentas_Stock.setText(""+p.getCantidadBodega());
                    jTextFieldVenta_Cantidad.requestFocus();
                }
                else{
                    LimpiarVenta();
                    jTextFieldVentas_Codigo.requestFocus();
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Ingrese el codigo del producto");
            }
        }
    }//GEN-LAST:event_jTextFieldVentas_CodigoKeyPressed

